package chap_01;

public class _02_DataTypes {
    public static void main(String[] args) {
        System.out.println("My name is jintae"); // string
        System.out.println(369); // int
        System.out.println(-369); // int
        System.out.println(3.14); // int
        System.out.println(true); // boolean
        System.out.println(false); // boolean

        System.out.println(333 + 666); // operation
    }
}
